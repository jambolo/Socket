#include "stdafx.h"

#include "Socket.h"

#include "SocketClient.h"



/********************************************************************************************************************/
/*																													*/
/*																													*/
/********************************************************************************************************************/

Socket::Socket( SocketClient * pNotifyClient )
	: m_pNotifyClient( pNotifyClient )
{
}



/********************************************************************************************************************/
/*																													*/
/*																													*/
/********************************************************************************************************************/

Socket::~Socket()
{

}



/********************************************************************************************************************/
/*																													*/
/*																													*/
/********************************************************************************************************************/

void Socket::OnAccept( int nErrorCode ) 
{
	if ( m_pNotifyClient )
		m_pNotifyClient->OnAccept( this, nErrorCode );
	else
		CSocket::OnAccept( nErrorCode );
}



/********************************************************************************************************************/
/*																													*/
/*																													*/
/********************************************************************************************************************/

void Socket::OnReceive( int nErrorCode ) 
{
	if ( m_pNotifyClient )
		m_pNotifyClient->OnReceive( this, nErrorCode );
	else
		CSocket::OnReceive( nErrorCode );
}



/********************************************************************************************************************/
/*																													*/
/*																													*/
/********************************************************************************************************************/

void Socket::OnClose( int nErrorCode ) 
{
	if ( m_pNotifyClient )
		m_pNotifyClient->OnClose( this, nErrorCode );
	else
		CSocket::OnClose( nErrorCode );
}
